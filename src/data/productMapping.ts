export const translateProduct = (rawProduct: string): string => {
  // Tenta extrair o código (ex: CO1253)
  const codeMatch = rawProduct.match(/^([A-Z]{2}\d{4})/);
  if (codeMatch) {
    const code = codeMatch[1];
    if (PRODUCT_MAPPING[code]) {
      return PRODUCT_MAPPING[code];
    }
  }
  
  // Se não achar pelo código, retorna o original limpo
  return rawProduct.replace(/^\w{2}\d{4}-/, '').split('(')[0].trim();
};
